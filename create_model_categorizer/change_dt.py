def get_cyclic_product(category):
    products = categories_to_products[category]
    while True:
        for product in products:
            yield product

def generate_balance_change(category):
    if category == 'Зарплата':
        return round(random.uniform(1000, 5000), 2)
    else:
        return round(random.uniform(-5000, -10), 2)

total_entries = 15000

data = {
    'Категория': [],
    'Изменение баланса': [],
    'Баланс': [round(random.uniform(0, 100000), 2) for _ in range(total_entries)],
    'День покупки': [random.randint(1, 31) for _ in range(total_entries)],
    'Месяц покупки': [random.randint(1, 12) for _ in range(total_entries)],
    'Продукт': []
}

category_counts = total_entries // len(categories_to_products)

for category in categories_to_products.keys():
    product_generator = get_cyclic_product(category)
    for _ in range(category_counts):
        data['Категория'].append(category)
        data['Продукт'].append(next(product_generator))
        data['Изменение баланса'].append(generate_balance_change(category))

remaining_entries = total_entries - len(data['Категория'])
if remaining_entries > 0:
    for category in random.choices(list(categories_to_products.keys()), k=remaining_entries):
        product_generator = get_cyclic_product(category)
        data['Категория'].append(category)
        data['Продукт'].append(next(product_generator))
        data['Изменение баланса'].append(generate_balance_change(category))

df = pd.DataFrame(data)

